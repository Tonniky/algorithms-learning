package DesignPattern.createMode.Builder;

public class Person {

    public String head;

    public String body;

    public String foot;

    public void setBody(String body) {
        this.body = body;
    }

    public String getBody() {
        return body;
    }

    public void setFoot(String foot) {
        this.foot = foot;
    }

    public String getFoot() {
        return foot;
    }

    public void setHead(String head) {
        this.head = head;
    }

    public String getHead() {
        return head;
    }
}
